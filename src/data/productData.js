const getProduct = (count, cond) => {
  const max = product.length - count
  const start = Math.floor(Math.random() * max)
  const products = product.filter(pd => pd.condition !== cond)
  return products.slice(start, start + count)
}

const getProductById = id => product.find(pd => pd._id === id)

const getProductByType = (type, cond) => product.filter(pd => pd.type === type && pd.condition !== cond)

const getCartItemsDetail = cartItems => {
  let res = []
  if(cartItems.length > 0) {
    cartItems.forEach(e => {
      res.push({
        ...e,
        product: getProductById(e._id)
      })
    })
  }
  return res
}

